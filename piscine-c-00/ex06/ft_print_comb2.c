#include <unistd.h>

void	ft_print_comb2(void)
{
	int	j;
	int	i;
	char	
		tab[5];

	i = 0;
	while (i <= 99)
	{
		j = i + 1;
		while (j < 100)
		{
			tab[0] = (i / 10) + '0';
			tab[1] = (i % 10) + '0';
			tab[2] = ' ';
			tab[3] = (j / 10) + '0';
			tab[4] = (j % 10) + '0';
			write(1, tab, 5);
			if (i != 98 || j != 99)
				write (1, ", ", 2);
			j++;
		}
		i++;
	}
}
